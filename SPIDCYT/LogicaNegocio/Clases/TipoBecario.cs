using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

/// <summary>
/// Determina el Tipo de Becario.
/// Tipos:
/// #Rectorado
/// #BINID
/// #SAE
/// #Otros
/// </summary>
public class TipoBecario
    {
        private int id;
        private String nombre;
        private String descripcion;

        public String DESCRIPCION
        {
            get { return descripcion; }
            set { descripcion = value; }
        }

        public String NOMBRE
        {
            get { return nombre; }
            set { nombre = value; }
        }
        public int ID
        {
            get { return id; }
            set { id = value; }
        }
    }
